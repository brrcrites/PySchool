skin = input("What type of skin does the animal have? ")

if skin == "fur":
    print("mammal")
elif skin == "feathers":
    print("bird")
elif skin == "scales":
    print("fish")
elif skin == "dry skin":
    print("reptile")
elif skin == "moist skin":
    print("amphibian")
else:
    print("not a proper skin type")
